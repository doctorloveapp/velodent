import { describe, expect, it } from "vitest";
import { parseTsCnsTlv } from "./tsCns";

describe("TS-CNS TLV parser", () => {
  it("extracts patient fields from ISO-7816 TLV tags", () => {
    const payload = new Uint8Array([
      0x5f, 0x20, 0x05, 0x52, 0x4f, 0x53, 0x53, 0x49,
      0x5f, 0x21, 0x05, 0x4d, 0x41, 0x52, 0x49, 0x4f,
      0x5f, 0x24, 0x08, 0x31, 0x39, 0x38, 0x35, 0x30, 0x38, 0x30, 0x31,
      0x5f, 0x25, 0x10, 0x52, 0x53, 0x53, 0x4d, 0x52, 0x41, 0x38, 0x35,
      0x4d, 0x30, 0x31, 0x48, 0x35, 0x30, 0x31, 0x51
    ]);

    expect(parseTsCnsTlv(payload)).toEqual({
      last_name: "ROSSI",
      first_name: "MARIO",
      date_of_birth: "1985-08-01",
      tax_code: "RSSMRA85M01H501Q"
    });
  });
});

